void	normalise(std::vector<float>& vector)
{
	for (std::vector<float>::iterator it = vector.begin() ; it != vector.end() ; ++it)
		*it == '#' ? *it = 1 : *it = 0;
}

void	denormalise(std::vector<float>& vector)
{
	for (std::vector<float>::iterator it = vector.begin() ; it != vector.end() ; ++it)
		*it == 1 ? *it = '#' : *it = ' ';
}

